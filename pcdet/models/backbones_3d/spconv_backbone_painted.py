def painted_point_cloud(calib_path,img_path,point_cloud,coords,num):

    '''
     Input: calib_path :store the projection params of camera and lidar
            img_path : store the img path
            point_cloud : store all point cloud from batch[0,N-1] shape:[N,3]
            coords: [N,4] store each point cloud's batch,voxel_id_x,y,z
            num : frame id of lidar [N,1]
    Output:
            point_cloud_painted [N,6] x,y,z,r,g,b
            painted_coords [N,4] b_idx,,voxel_id_x,y,z
    '''
    point_cloud = point_cloud.cpu().numpy()
    coords = coords.cpu().numpy()
    painted_point_cloud = []
    painted_coords = []
    batch_idx = 0
    for b in num:
        idx = np.where(coords[:, 0] == batch_idx)[0]
        pc = point_cloud[idx]
        cor = coords[idx]
        calib_path_b = calib_path + str(b).zfill(6)+ '.txt'
        img_path_b = img_path+ str(b).zfill(6) + '.png'
        non_ground_pointcloud = pc
        batch_idx += 1

        calib = calibration_kitti.Calibration(calib_path_b)
        img = cv2.imread(img_path_b)
        img_shape = img.shape
        pts_img,pts_rect_depth = calib.lidar_to_img(non_ground_pointcloud) # [N,3] in lidar to [N,2] in img
        pts_img = np.round(pts_img).astype(int)#四舍五入

        #after lidar to img,filtering points in img's range ;
        val_flag_1 = np.logical_and(pts_img[:, 0] >= 0, pts_img[:, 0] < img_shape[1])
        val_flag_2 = np.logical_and(pts_img[:, 1] >= 0, pts_img[:, 1] < img_shape[0])
        val_flag_merge = np.logical_and(val_flag_1, val_flag_2)
        pts_valid_flag = np.logical_and(val_flag_merge, pts_rect_depth >= 0)
        pts_non_valid_flag = ~pts_valid_flag

        ## in img's range ; all lidar's nonground points##
        pts_img = pts_img[pts_valid_flag]
        pts_img[:, [0, 1]] = pts_img[:, [1, 0]]# height,width to width height[1242,375] to [375,1242]
        row = pts_img[:,0]
        col = pts_img[:,1]
        img = img.astype(int)
        non_ground_pointcloud_color = img[row,col,:] # [N,3] b g r
        non_ground_pointcloud_color[:,[0,1,2]] = non_ground_pointcloud_color[:,[2,1,0]]
        coords_in_img = cor[pts_valid_flag]
        ## points not in img range ,its colors set [0,0,0]
        non_valid_point_cloud = non_ground_pointcloud[pts_non_valid_flag]
        non_valid_point_cloud_color = np.zeros((non_valid_point_cloud.shape[0], 3))
        painted_point_cloud_non_b = np.hstack((non_valid_point_cloud, non_valid_point_cloud_color))  # [N,6] x,y,z,r,g,b
        coords_not_in_img = cor[pts_non_valid_flag]
        ## points in img range
        painted_point_cloud_b = np.hstack((non_ground_pointcloud[pts_valid_flag], non_ground_pointcloud_color))  # [N,6] x,y,z,r,g,b
        ## painted point cloud = points in img + point not in img
        painted_point_cloud_b = np.vstack((painted_point_cloud_b, painted_point_cloud_non_b))
        painted_coords_b = np.vstack((coords_in_img,coords_not_in_img))
        if batch_idx == 1:
            painted_point_cloud = painted_point_cloud_b
            painted_coords = painted_coords_b
        else:
            painted_point_cloud = np.vstack((painted_point_cloud,painted_point_cloud_b))
            painted_coords = np.vstack((painted_coords,painted_coords_b))

    painted_point_cloud = np.array(painted_point_cloud).reshape((-1,6))
    painted_coords = np.array(painted_coords).reshape((-1,4))

    return painted_point_cloud,painted_coords

def post_act_block(in_channels, out_channels, kernel_size, indice_key=None, stride=1, padding=0,
                   conv_type='subm', norm_fn=None):

    if conv_type == 'subm':
        conv = spconv.SubMConv3d(in_channels, out_channels, kernel_size, bias=False, indice_key=indice_key)
    elif conv_type == 'spconv':
        conv = spconv.SparseConv3d(in_channels, out_channels, kernel_size, stride=stride, padding=padding,
                                   bias=False, indice_key=indice_key)
    elif conv_type == 'inverseconv':
        conv = spconv.SparseInverseConv3d(in_channels, out_channels, kernel_size, indice_key=indice_key, bias=False)
    else:
        raise NotImplementedError

    m = spconv.SparseSequential(
        conv,
        norm_fn(out_channels),
        nn.ReLU(),
    )

    return m


class SparseBasicBlock(spconv.SparseModule):
    expansion = 1

    def __init__(self, inplanes, planes, stride=1, norm_fn=None, downsample=None, indice_key=None):
        super(SparseBasicBlock, self).__init__()

        assert norm_fn is not None
        bias = norm_fn is not None
        self.conv1 = spconv.SubMConv3d(
            inplanes, planes, kernel_size=3, stride=stride, padding=1, bias=bias, indice_key=indice_key
        )
        self.bn1 = norm_fn(planes)
        self.relu = nn.ReLU()
        self.conv2 = spconv.SubMConv3d(
            planes, planes, kernel_size=3, stride=stride, padding=1, bias=bias, indice_key=indice_key
        )
        self.bn2 = norm_fn(planes)
        self.downsample = downsample
        self.stride = stride

    def forward(self, x):
        identity = x

        out = self.conv1(x)
        out.features = self.bn1(out.features)
        out.features = self.relu(out.features)

        out = self.conv2(out)
        out.features = self.bn2(out.features)

        if self.downsample is not None:
            identity = self.downsample(x)

        out.features += identity.features
        out.features = self.relu(out.features)

        return out


class VoxelBackBone8x_painted(nn.Module):
    def __init__(self, model_cfg, input_channels, grid_size, **kwargs):
        super().__init__()
        self.model_cfg = model_cfg
        norm_fn = partial(nn.BatchNorm1d, eps=1e-3, momentum=0.01)

        self.sparse_shape = grid_size[::-1] + [1, 0, 0]

        self.conv_input = spconv.SparseSequential(
            spconv.SubMConv3d(6, 16, 3, padding=1, bias=False, indice_key='subm1'),
            norm_fn(16),
            nn.ReLU(),
        )
        block = post_act_block

        self.conv1 = spconv.SparseSequential(
            block(16, 16, 3, norm_fn=norm_fn, padding=1, indice_key='subm1'),
        )

        self.conv2 = spconv.SparseSequential(
            # [1600, 1408, 41] <- [800, 704, 21]
            block(16, 32, 3, norm_fn=norm_fn, stride=2, padding=1, indice_key='spconv2', conv_type='spconv'),
            block(32, 32, 3, norm_fn=norm_fn, padding=1, indice_key='subm2'),
            block(32, 32, 3, norm_fn=norm_fn, padding=1, indice_key='subm2'),
        )

        self.conv3 = spconv.SparseSequential(
            # [800, 704, 21] <- [400, 352, 11]
            block(32, 64, 3, norm_fn=norm_fn, stride=2, padding=1, indice_key='spconv3', conv_type='spconv'),
            block(64, 64, 3, norm_fn=norm_fn, padding=1, indice_key='subm3'),
            block(64, 64, 3, norm_fn=norm_fn, padding=1, indice_key='subm3'),
        )

        self.conv4 = spconv.SparseSequential(
            # [400, 352, 11] <- [200, 176, 5]
            block(64, 64, 3, norm_fn=norm_fn, stride=2, padding=(0, 1, 1), indice_key='spconv4', conv_type='spconv'),
            block(64, 64, 3, norm_fn=norm_fn, padding=1, indice_key='subm4'),
            block(64, 64, 3, norm_fn=norm_fn, padding=1, indice_key='subm4'),
        )

        last_pad = 0
        last_pad = self.model_cfg.get('last_pad', last_pad)
        self.conv_out = spconv.SparseSequential(
            # [200, 150, 5] -> [200, 150, 2]
            spconv.SparseConv3d(64, 128, (3, 1, 1), stride=(2, 1, 1), padding=last_pad,
                                bias=False, indice_key='spconv_down2'),
            norm_fn(128),
            nn.ReLU(),
        )
        self.num_point_features = 128

    def forward(self, batch_dict):
        """
        Args:
            batch_dict:
                batch_size: int
                vfe_features: (num_voxels, C)
                voxel_coords: (num_voxels, 4), [batch_idx, z_idx, y_idx, x_idx]
        Returns:
            batch_dict:
                encoded_spconv_tensor: sparse tensor
        """
        voxel_features, voxel_coords = batch_dict['voxel_features'], batch_dict['voxel_coords']
        batch_size = batch_dict['batch_size']
        img_idx_set = batch_dict['frame_id']
        calib_path = '/media/ddd/data2/3d_MOTS_Ex./Code/OpenPCDet/data/kitti/training/calib/'
        img_path = '/media/ddd/data2/3d_MOTS_Ex./Code/OpenPCDet/data/kitti/training/image_2/'
        voxel_features, voxel_coords = painted_point_cloud(calib_path,img_path,voxel_features,voxel_coords,img_idx_set)
        voxel_features = torch.from_numpy(voxel_features).float().cuda()
        voxel_coords = torch.from_numpy(voxel_coords).float().cuda()
        input_sp_tensor = spconv.SparseConvTensor(
            features=voxel_features,
            indices=voxel_coords.int(),
            spatial_shape=self.sparse_shape,
            batch_size=batch_size
        )

        x = self.conv_input(input_sp_tensor)

        x_conv1 = self.conv1(x)
        x_conv2 = self.conv2(x_conv1)
        x_conv3 = self.conv3(x_conv2)
        x_conv4 = self.conv4(x_conv3)

        # for detection head
        # [200, 176, 5] -> [200, 176, 2]
        out = self.conv_out(x_conv4)

        batch_dict.update({
            'encoded_spconv_tensor': out,
            'encoded_spconv_tensor_stride': 8
        })
        batch_dict.update({
            'multi_scale_3d_features': {
                'x_conv1': x_conv1,
                'x_conv2': x_conv2,
                'x_conv3': x_conv3,
                'x_conv4': x_conv4,
            }
        })

        return batch_dict


